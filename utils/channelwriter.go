package utils

type chanWriter struct {
	ch chan byte
}

func NewChanWriter() *chanWriter {
	return &chanWriter{make(chan byte, 2048)}
}

func (w *chanWriter) Chan() <-chan byte {
	return w.ch
}

func (w *chanWriter) Write(p []byte) (int, error) {
	n := 0
	for _, b := range p {
		w.ch <- b
		n++
	}
	return n, nil
}

func (w *chanWriter) Close() error {
	close(w.ch)
	return nil
}
